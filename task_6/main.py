import random

# 1) создаём список из 20 кортежей по 4 случайных числа в диапазоне -10..10
data = [tuple(random.randint(-10, 10) for _ in range(4)) for _ in range(20)]

# 2) получаем "уникальные комбинации без учёта порядка" — сортируем элементы внутри кортежа
#    и кладём в множество, затем обратно в список кортежей для вывода
unique_combinations = list({tuple(sorted(t)) for t in data})

# печатаем исходный список и уникальные комбинации
print("Исходный список (20 кортежей по 4 числа):")
for t in data:
    print(t)
print("\nУникальные комбинации (порядок внутри не учитывается):")
print(unique_combinations)

# 3) запрашиваем у пользователя целое число, с проверкой ввода
while True:
    s = input("\nВведите целое число (порог суммы T): ")
    try:
        T = int(s)
        break
    except ValueError:
        print("Нужно ввести целое число. Попробуй ещё раз.")

# 4) считаем количество пар (i < j), у которых сумма элементов пары < T
#    здесь суммой элемента считаем сумму 4 чисел в кортеже
sums = [sum(t) for t in data]  # сумма каждого кортежа
count = 0
n = len(sums)
for i in range(n):
    for j in range(i + 1, n):
        if sums[i] + sums[j] < T:
            count += 1

print(f"\nКоличество пар с суммой меньше {T}: {count}")