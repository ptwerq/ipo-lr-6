import random  # Импортируем модуль random для случайных чисел

# Создаём список возможных значений для заполнения матрицы
values = [-3, -5, -2, -12, 0, 15, 4, 7, 2]

# Случайно выбираем размер матрицы по высоте (строки) от 4 до 8
rows = random.randint(4, 8)

# Случайно выбираем размер матрицы по ширине (столбцы) от 4 до 8
cols = random.randint(4, 8)

# Создаём матрицу с помощью вложенных списков
# Для каждой строки создаём список из 'cols' элементов, выбирая случайные значения из 'values'
matrix = [[random.choice(values) for _ in range(cols)] for _ in range(rows)]

# Выводим размер матрицы для наглядности
print(f"Матрица {rows}x{cols}:")

# Выводим матрицу в форматированном виде
for row in matrix:
    for elem in row:
        # Каждый элемент выравниваем по ширине 4 символа
        print(f"{elem:4}", end=" ")
    print()  # Переход на новую строку после каждой строки матрицы

# Создаём переменную для хранения суммы чётных элементов
even_sum = 0

# Проходим по всем элементам матрицы
for row in matrix:
    for elem in row:
        # Проверяем, является ли число чётным
        if elem % 2 == 0:
            even_sum += elem  # Добавляем к сумме

# Выводим результат
print("\nСумма всех чётных элементов:", even_sum)