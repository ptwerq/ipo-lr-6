import random  # подключаем модуль для генерации случайных чисел

numbers = []  # создаём пустой список для чисел
for i in range(25):  # повторяем 25 раз
    numbers.append(random.randint(-50, 50))  # добавляем случайное число от -50 до 50

print("Список чисел:", numbers)  # выводим весь список

plus = 0  # счётчик положительных чисел
minus = 0  # счётчик отрицательных чисел
zero = 0  # счётчик нулей

for n in numbers:  # перебираем все числа
    if n > 0:  # если число положительное
        plus += 1  # увеличиваем плюс
    elif n < 0:  # если число отрицательное
        minus += 1  # увеличиваем минус
    else:  # если число равно нулю
        zero += 1  # увеличиваем нули

total = len(numbers)  # общее количество чисел

p_plus = plus / total * 100  # процент положительных
p_minus = minus / total * 100  # процент отрицательных
p_zero = zero / total * 100  # процент нулей

print("\nПоложительных чисел:", plus, f"({p_plus:.1f}%)")  # вывод положительных
print("Отрицательных чисел:", minus, f"({p_minus:.1f}%)")  # вывод отрицательных
print("Нулей:", zero, f"({p_zero:.1f}%)")  # вывод нулей

print("\nМаксимальное число:", max(numbers))  # вывод максимального числа
print("Минимальное число:", min(numbers))  # вывод минимального числа
