# Создаём список из 6 строк со значениями
words = ["apple", "banana", "cherry", "grape", "melon", "kiwi"]

# Создаём переменную-счётчик, чтобы считать, сколько строк содержат букву 'a'
count = 0

# Запускаем цикл, который перебирает каждое слово в списке
for word in words:
    # Проверяем, есть ли буква 'a' в текущем слове
    if "a" in word:
        # Если есть, увеличиваем счётчик на 1
        count += 1

# После цикла выводим количество строк, где встречается буква 'a'
print("Количество строк, содержащих букву 'a':", count)