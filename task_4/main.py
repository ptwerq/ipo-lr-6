search = input("Введите подстроку для поиска: ")  # запрашиваем строку для поиска у пользователя

with open("text.txt", "r", encoding="utf-8") as f:  # открываем файл text.txt для чтения с кодировкой UTF-8
    lines = f.readlines()  # читаем все строки файла в список

found = [line.strip() for line in lines if search in line]  # создаём список строк, содержащих подстроку, убираем пробелы и переносы

print("\nНайденные строки:")  # выводим заголовок для найденных строк
for line in found:  # перебираем все найденные строки
    print(line)  # выводим каждую строку
print(f"\nКоличество найденных строк: {len(found)}")  # выводим количество найденных строк

found_sorted = sorted(found, key=len)  # сортируем найденные строки по длине (от короткой к длинной)

print("\nСтроки, отсортированные по длине:")  # заголовок для отсортированных строк
for line in found_sorted:  # перебираем отсортированные строки
    print(line)  # выводим каждую строку